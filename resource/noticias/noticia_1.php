            <div style="font-size: 18px; font-weight: normal;word-spacing: normal;">
            <p >Con una maleta cargada de preparación, sueños y anhelos partieron nuestros representantes como “Equipe Vulcao” en el mes de octubre de este año, con destino al “World Jiu-Jitsu Championship” de la CBJJP (confederação brasileira de jiu-jitsu profissional) a realizarse en Rio de Janeiro, Brasil.</p>
<p class="lead" style="color: #0a090a;">Luego de más de un mes de preparación que debió combinarse y adaptarse en algunos casos, con el trabajo, los estudios e incluso lesiones el día de la partida llegaba finalmente. En dos vuelos diferentes dejaron Chile nuestros cuatro representantes, quienes partieron con una semana y días de antelación al gran evento. Una vez en el país cuna de este deporte, la aclimatación fue pieza fundamental y para ello entrenamientos bajo las temperaturas y humedad que encontrarían en el día de la competencia fueron esenciales, uno o dos entrenamientos diarios marcaron la constante que siguió el equipo. No era sencillo, tomando en cuenta que las temperaturas promedio en la región de la Araucanía suele ser de 17° en los meses de septiembre y octubre, mientras que en Rio de Janeiro estas bordean los 27° o 28°.</p>
<p class="lead" style="color: #0a090a;">Luego de casi una semana y media en tierras cariocas, el “Equipe Vulcao” al fin ingreso al tatami del Club Municipal del barrio de Tijuca. Y vaya que si lo hicieron de forma excepcional, obteniendo los cuatro integrantes del equipo, un merecido puesto en los podios de sus respectivas categorías. Diego Nova y Matías Henríquez ambos Faixa Azul, de categorías “Master 1 Medio-Pesado” (hasta 88,3 kg) y “Adulto Pena” (hasta 70 kg) respectivamente, obtuvieron preseas de bronce. Un merecido tercer lugar para ambos, frente a rivales muy fuertes y competitivos, destacando incluso una sumisión por parte de Diego, que durmió (literalmente) a su oponente en la primera de sus luchas. Además de la merecida medalla ambos luchadores obtuvieron la graduación a Faixa Roxa, entregada por el maestro de nuestra academia Humberto Fuentes, mientras recibían sus medallas en el podio del evento.</p>
<p class="lead" style="color: #0a090a;">También estuvo en el podio, pero no de la forma que deseaba, el maestro Humberto Fuentes, quien debido a la falta de contendientes en su categoría (Master 4, 5 y 6 Preta Leve, hasta 76 kg.) y con el ferviente deseo de participar del evento y tener al menos una lucha en este, subió dos categorías para tener rival, hasta la Master 4, 5 y 6 Preta Medio-Pesado (hasta 88,3 kg.), en la cual fue vencido por una sumisión, ante un rival 12 kg por arriba en peso, quien jamás logro superar la guardia de Humberto, apostando únicamente a su fuerza para intentar encajar una sumisión que finalmente llego y logro su cometido. Sin embargo y pese a la derrota, la mayor cantidad de elogios por parte de los demás participantes y maestros presentes fueron para Humberto, a quien llamaban “Corajudo” por tal ferviente deseo por luchar, llevado hasta la cancha de tatami del Municipal de Tijuca.</p>
<p class="lead" style="color: #0a090a;">Y la presea más importante de este viaje, la mencionamos al final, pero fue la primera en llegar, ya que Naomi Medina, nuestra representante Faixa Azul de la categoría “Adulto Pluma” (hasta 53,5 kg.) fue quien primero pisó el tatami carioca, dejando una vara altísima, ya que tras dos luchas, frente a avezadas competidoras locales, logró igual número de victorias y ambas por sumisión, lo que le dio un realce más que merecido a la competidora temuquense, quien atrajo las miradas y felicitaciones de muchos, tras el destacado logro. Más que merecidas preseas y graduaciones, obtuvieron cada uno de nuestros cuatro representantes en la importante competencia internacional, pero no fue todo, además de eso cimentaron una idea que constantemente es planteada en nuestras academias, el nivel que manejamos dentro de esta disciplina es y demostradamente muy alto y podemos salir a competir sin temor o recelo si así nos lo planteamos. Por lo cual sabemos que el próximo desafío que nos presente el país de la samba o cualquier otro, recibirá a un mayor número de integrantes en el “Equipe Vulcao”.-</p></div>

